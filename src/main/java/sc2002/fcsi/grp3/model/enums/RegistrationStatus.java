package sc2002.fcsi.grp3.model.enums;

/**
 * The RegistrationStatus enum represents the various statuses a registration can have in the system.
 */
public enum RegistrationStatus {

    /**
     * The registration is pending and awaiting review.
     */
    PENDING,

    /**
     * The registration has been reviewed and approved.
     */
    APPROVED,

    /**
     * The registration has been reviewed and rejected.
     */
    REJECTED
}
